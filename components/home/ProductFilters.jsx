export default function ProductFilters({ translate, fetchProducts }) {
  const trans = useTranslations(translate);
  const t = (key) => trans(`home.filters.${key}`);
  const [selectedFilter, setSelectedFilter] = useState("main");
  const [loading, setLoading] = useState(false);

  // Array of filter objects with value, translated text, and icon
  const filters = [
    {
      value: "main",
      label: t("main"),
      Icon: Main,
      description: t("mainDesc"),
    },
    {
      value: "freeDelivery",
      label: t("freeDelivery"),
      Icon: freeDelivery,
      description: t("freeDeliveryDesc"),
    },
    // {
    //   value: "newest",
    //   label: t("newest"),
    //   Icon: Newest,
    //   description: t("newestDesc"),
    // },
    // {
    //   value: "discounts",
    //   label: t("discounts"),
    //   Icon: TagIcon,
    //   description: t("discountsDesc"),
    // },
    {
      value: "bestSelling",
      label: t("bestSelling"),
      Icon: TrendingUpIcon,
      description: t("bestSellingDesc"),
    },
    {
      value: "highestRated",
      label: t("highestRated"),
      Icon: StarIcon,
      description: t("highestRatedDesc"),
    },
  ];

  const handleFilterClick = async (filterValue) => {
    setSelectedFilter(filterValue);
    setLoading(true);
    await fetchProducts(
      filterValue === "main" ? { status: "main" } : filterValue === "freeDelivery" ? { deliveryType: "free" } : { sortBy: filterValue },
    );
    setLoading(false);
  };

  return (
    <section
      className="flex flex-wrap gap-x-2 md:gap-x-8 gap-y-4 justify-center md:mb-12 mb-6 -mt-2"
      aria-label={t("filtersLabel")}
    >
      {filters.map(({ value, label, Icon, description }) => (
        <button
          key={value}
          onClick={() => handleFilterClick(value)}
          disabled={loading}
          aria-pressed={selectedFilter === value}
          aria-describedby={`filter-desc-${value}`}
          title={description}
          className={`
              flex items-center md:gap-2 gap-1 px-2 py-2 border-b-2 focus:outline-primary transition-all duration-300 disabled:opacity-70 ${
                selectedFilter === value
                  ? "border-b-primary text-primary"
                  : "border-b-transparent text-[#5B5656] hover:border-b-gray-300"
              }
            `}
        >
          {loading && selectedFilter === value ? (
            <div
              className="md:w-6 w-5 md:h-6 h-5 border-2 md:me-1 border-primary border-t-transparent rounded-full animate-spin"
              role="status"
              aria-label={t("loading")}
            ></div>
          ) : (
            <span className="text-sm" aria-hidden="true">
              <Icon color={selectedFilter === value ? "#f48a42" : "#5B5656"} />
            </span>
          )}
          <span className="md:text-lg text-sm">{label}</span>
          <span id={`filter-desc-${value}`} className="sr-only">
            {description}
          </span>
        </button>
      ))}
    </section>
  );
}
